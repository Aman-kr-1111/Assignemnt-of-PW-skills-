// Q1 - Take 2 integer values in two variables x and y and print their product.

#include <iostream>
using namespace std;
int main()
{
    int num1, num2;
    cout << "Enter your 1st number : ";
    cin >> num1;

    cout << "Enter your 2nd number : ";
    cin >> num2;
    cout << num1 << " * " << num2 << " = " << num1 * num2;
    return 0;
}